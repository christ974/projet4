    <h2>
        PRÉAMBULE
    </h2>
    <p>
        A.E.E est particulièrement attaché au respect de la vie privée et à la protection des données à caractère personnel. C’est pourquoi ces valeurs constituent un des engagements sociétaux de A.E.E au sein de sa Politique de Développement Durable.
        A.E.E s’engage, conformément à la règlementation en vigueur en France et en Europe, à mettre en œuvre des mesures adéquates pour assurer la protection, la confidentialité et la sécurité des données à caractère personnel de toute personne auprès de laquelle des données sont collectées.
        Pour veiller à la mise en œuvre de ces mesures, A.E.E a désigné en 2010 un Correspondant Informatique et Libertés (CIL), votre interlocuteur privilégié pour toute question relative au traitement des données vous concernant et le référent auprès de la Commission Nationale de l’Informatique et des Libertés (CNIL).
        La présente Politique de Protection des Données a pour objectif de vous informer sur les engagements pris par A.E.E afin de veiller au respect de vos données à caractère personnel. Cette Politique pourra évoluer en fonction du contexte légal et réglementaire et de la doctrine de la CNIL.
    </p>
    <h2>
        CHAMP d’APPLICATION
    </h2>
    <p>
        Cette Politique de Protection des Données à caractère personnel s’applique à tous les traitements de données à caractère personnel, mis en œuvre par A.E.E, relatifs aux personnes extérieures (notamment les clients, partenaires, candidats au recrutement, fournisseurs, internautes).
    </p>
    <h2>
        DÉFINITIONS
    </h2>
    <p>
        « Données à caractère personnel » / « Données personnelles » : toute donnée permettant d’identifier directement ou indirectement une personne physique.
        Traitement de données » : opérations réalisées sur des données à caractère personnel, notamment la collecte, l’enregistrement, l’utilisation, la transmission ou la communication.
        « Responsable de traitement » : entité juridique en charge de déterminer les finalités du traitement et les moyens mis en œuvre.
    </p>
    <h2>
        DONNÉES COLLECTÉES
    </h2>
    <p>
        De manière générale, vous pouvez entrer en contact avec A.E.E et visiter les sites internet du groupe sans qu’il soit nécessaire de nous communiquer vos données personnelles.
        Toutefois, afin d’être en mesure de vous apporter certains services, A.E.E peut vous demander de lui fournir des données personnelles, par exemple, pour répondre à une demande de contact, suivre un dossier de candidature, envoyer une newsletter ou gérer votre abonnement à l’un de nos dispositifs de vélos en libre-service.
        Peuvent notamment être collectées vos coordonnées (nom, adresse, numéro de téléphone, adresse électronique, etc.) et d’autres informations, telles que celles indispensables à la création de votre compte lorsque vous vous abonnez à un de nos systèmes de vélos en libre-service ou des données liées à votre parcours professionnel lorsque vous postulez sur nos sites.
        Dans tous les cas, A.E.E veille à collecter et à traiter des données personnelles pertinentes, adéquates, non excessives et strictement nécessaires à l’atteinte des finalités qui ont été préalablement déterminées. A.E.E prend toutes les dispositions utiles pour que vos données soient exactes, complètes et, si nécessaire, mises à jour.
    </p>
    <h2>
        FINALITÉS de la COLLECTE
    </h2>
    <p>
        A.E.E s’engage à collecter et traiter vos Données personnelles de façon loyale et licite.
        Les traitements mis en œuvre par A.E.E répondent à des finalités explicites, légitimes et déterminées.
        Vos données peuvent notamment être traitées pour les finalités suivantes :
        suivi de votre dossier de candidature déposé en ligne sur l’un de nos sites Internet ;
        diffusion de nos newsletters ;
        réponse à vos demandes de contacts ;
        gestion de votre abonnement à l’un de nos services de vélos en libre-service.
    </p>
    <h2>
        INFORMATION des PERSONNES
    </h2>
    <p>
        Conformément à la loi Informatique et Libertés, A.E.E s’engage à vous informer sur le traitement de vos données personnelles.
        Ainsi, A.E.E vous informe, de manière claire, lors de chaque collecte de vos données, notamment :
        de l’identité du responsable de traitement (il s’agit de l’entité juridique de A.E.E responsable du traitement de vos données) ;
        de la finalité pour laquelle vos données sont collectées ;
        du caractère facultatif ou obligatoire des réponses ;
        du (des) destinataire(s) des données ;
        de l’existence et des modalités d’exercice des droits d’interrogation, d’accès, de rectification et d’opposition relativement à l’ensemble des données vous concernant.
    </p>
    <h2>
        DESTINATAIRES des DONNÉES COLLECTÉES
    </h2>
    <p>
        A.E.E ne communique vos données à caractère personnel qu’à des destinataires habilités et déterminés.
        Les destinataires de vos données sont les services concernés de A.E.E, ainsi que, le cas échéant, les personnels habilités de sociétés partenaires ou prestataires.
    </p>
    <h2>
        CONSERVATION des DONNÉES
    </h2>
    <p>
        A.E.E conserve vos données pour la durée nécessaire aux opérations pour lesquelles elles ont été collectées, dans le respect de la législation en vigueur.
    </p>
    <h2>
        COOKIES
    </h2>
    <p>
        Les cookies sont des données stockées dans l’équipement terminal d’un Internaute et utilisées par un site Internet pour envoyer des informations au navigateur de l’Internaute, et permettant à ce navigateur de renvoyer des informations au site d’origine (par exemple un identifiant de session, le choix d’une langue ou une date).
        Vous êtes informé(e) que, lors de vos visites sur nos sites internet, des cookies (témoins de connexion) peuvent être installés sur votre logiciel de navigation.
    </p>
    <h2>
        SECURITÉ
    </h2>
    <p>
        A.E.E assure la sécurité de vos données à caractère personnel en mettant en place une protection des données par l’utilisation de moyens de sécurisation physiques et logiques.
    </p>
    <h2>
        DROITS des PERSONNES CONCERNÉES
    </h2>
    <p>
        En application de la loi Informatique et Libertés du 6 janvier 1978 modifiée, toute personne dont les données ont été collectées par A.E.E dispose d’un droit d’interrogation, d’accès, de rectification et d’opposition pour motifs légitimes relativement à l’ensemble des données la concernant. Ces droits s’exercent auprès du Correspondant Informatique et Libertés du groupe A.E.E, par courrier électronique à l’adresse suivante : pdp@A.E.E.fr ou par courrier postal à l’adresse suivante : Correspondant Informatique et Libertés du groupe A.E.E, 17 rue Soyer, 92200 Neuilly-sur-Seine, accompagné d’une copie d’un titre d’identité signé.
    </p>
    <h2>
        FORMALITÉS
    </h2>
    <p>
        Les traitements de données à caractère personnel font l’objet de formalités auprès de notre Correspondant Informatique et Libertés (CIL) ou, si requis, auprès de la Commission Nationale de l’Informatique et des Libertés (CNIL).
    </p>
    <h2>
        CONTACT
    </h2>
    <p>
        Pour toute demande d’information relative à cette Politique de protection des données à caractère personnel et de la vie privée, vous pouvez vous adresser à pdp@A.E.E.fr.

        Cette Politique peut être mise à jour à tout moment, aussi nous vous conseillons de la consulter régulièrement.
